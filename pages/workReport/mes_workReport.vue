<script>
import { arrayToHeavy, toasting,touchStart, touchMove, touchEnd} from '../../utils/index.js'
import Api from '../../service/api'
import scanCode from "../../components/scan/scan.vue"

export default{
	// name: 'cutOutStorage',
	onLoad() {
		// console.log('onLoad');
	},
	onShow() {
		// console.log('onShow');
		uni.$off('scancodedate') // 每次进来先 移除全局自定义事件监听器  
		uni.$on('scancodedate', (data) => {
			console.log(data)
			// if(this.sewingTaskRecord) {
			// 	console.log("扫描PCS码")
			// 	// 扫描PCS码
				this.handleScanPCS(data.code)
			// }else {
			// 	console.log("扫描缝制任务单号")
			// 	// 扫描缝制任务单号
			// 	this.handleScanTask(data.code)
			// }
		})
	},
	data(){
		return{
			isSelectCheckbox: true,
			showModal: false,
			showSuccessPop: false,
			showErrorPop: false,
			showSuccessMessage: '',
			showErrorMessage: '',
			productNum: '',
			supplierName:'',
			supplierId:'',
			outStorageArr: [],
			alreadyOutStorageArr: [],
			alreadyCount:0,
			PCSData:[],
			selectIndex: 0,
			title:'标题',
			columns:[
				['中国','美国','日本']
			],
			show:false
		}
	},
	methods:{
		//事件委托全局按钮
		handleClick(e){
			if(e.target.id!=="btnModal"&& e.target.id!=="moreBtn"&& this.showModal){
				this.showModal=false
			}
		},
		handleConfirm(e){	//报工工段确认
			this.productNum=e.value[0]
			this.show=false
		},
		handleCancel(){		//报工工段取消
			this.show=false
		},
		handleScanPCS(pcsNum){ // 扫描PCS码
			this.outStorageArr = this.outStorageArr.reverse()
			Api.getInfoBySearch({
				pcsNum, // 'PD20211118073139826-0-00153638'
				type: this.typeMode,
				wareHouseLocation: this.wareHouseLocation
			}).then(res => {
				if (res.code === 0) {
					uni.showToast({
						title: '扫描PCS码成功！',
						icon: 'none',
						duration: 3000
					})
					this.outStorageArr.push(
						{
							id: res.data.subpackageId || "",
							productId: res.data.productId || "",
							produceId: res.data.produceId || "",
							subpackageId: res.data.subpackageId || "",
							proNum: res.data.proNum || "",
							colorCode: res.data.colorCode || "",
							colorName: res.data.colorName || "",
							sizeCode: res.data.sizeCode || "",
							sizeName: res.data.sizeName || "",
							packageNum: res.data.packageNum || "",
							inputNumber: res.data.inputNumber || "",
							arrowFlag: true
						}
					)
					
					// 数组去重
					this.outStorageArr = arrayToHeavy(this.outStorageArr)
					
					this.outStorageArr = this.outStorageArr.reverse()
				}else {
					this.showErrorMessage = res.msg
					this.showErrorPop = true
					let timer = setTimeout(() => {
						clearTimeout(timer)
						this.showErrorPop = false
					}, 2000)
				}
			})
		},
		
		
		handleMore(){ // 更多
			this.showModal = !this.showModal
		},
		
		handleOutStorage(){ // 出库
			Api.outOutsourcingDelivery({
				mesOrderSubpackageOutwardDTOList:this.PCSData
			}).then(res => {
				if (res.code === 0) {
					this.outStorageArr=this.outStorageArr.filter(item=>{
						this.alreadyOutStorageArr.forEach(val=>{
							return item.packageNum!==val.packageNum
						})
					})
					this.alreadyOutStorageArr=[]
					this.PCSData=[]
					this.productNum = ''
					this.supplierName=""
					this.alreadyCount=0
					this.showSuccessMessage = '出库并打印成功！'
					this.showSuccessPop = true
					let timer = setTimeout(() => {
						clearTimeout(timer)
						this.showSuccessPop = false
					}, 2000)
				}
			})
		},
		
		handleEmpty(){ // 清空
			this.outStorageArr = []
			this.PCSData=[]
			this.showModal = false
			this.productNum = ''
			this.supplierName=""
			this.alreadyCount=0
			this.alreadyOutStorageArr=[]
		},
	},
	components: {
		scanCode
	}
};
</script>

